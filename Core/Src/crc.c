uint16_t GetCrc16(const uint8_t* pData, uint16_t nLength)
{
    uint16_t  fcs  =  0xffff;         //  初始化
    while(nLength>0){
        fcs = (fcs >> 8) ^ crctab16[(fcs ^ *pData) & 0xff];
        nLength--;
        pData++;
    }
	fcs=~fcs;
	fcs = ((fcs<<8)&0xff00)+((fcs>>8)&0xff);
    return  fcs;         //  取反
}


