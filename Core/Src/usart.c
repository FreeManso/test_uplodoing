void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	uint16_t num_massage = 0;
	
	num_massage = sizeof(send_res);
	
	send_res.len = 12;//数据长度
	send_res.ver = 0x11;//高4位硬件版本号和低4位软件版本号
	
	send_res.photoelectricity[0]=DMA_TATA_BUFFER[0];		//光电
	send_res.photoelectricity[1]=DMA_TATA_BUFFER[1];		
	send_res.photoelectricity[2]=DMA_TATA_BUFFER[2];
	send_res.photoelectricity[3]=DMA_TATA_BUFFER[3];
	
	send_res.Hall_switch[0]=DMA_TATA_BUFFER[4];		//霍尔
	send_res.Hall_switch[1]=DMA_TATA_BUFFER[5];
	
	send_res.times++;
	
	send_res.crc = HAL_CRC_Calculate(&hcrc,(uint32_t * )&send_res,3);
	
	HAL_UART_Transmit_DMA(&huart1,(uint8_t * )&send_res,num_massage);
	
	int recv_len = X_UART1_Get_NewBuf(recv_buf);
	
	if(recv_len > 0)
	{
		if((recv_buf[0]=='r')&&(recv_buf[1]=='e')&&(recv_buf[2]=='b')&&(recv_buf[3]=='o')&&(recv_buf[4]=='o')&&(recv_buf[5]=='t'))
		{
			NVIC_SystemReset(); 
		}
	}
	HAL_GPIO_TogglePin(STATE_GPIO_Port, STATE_Pin);
}
